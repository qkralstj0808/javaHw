public class Loops {
    public static void main(String[] args) {
        // for
        for (int i = 0; i< 10; i++){
            System.out.println(i);
        }

        System.out.println();
        // while
        int a = 10;
        while (a > 0){
            System.out.println(a);
            a--;
        }
    }
}
